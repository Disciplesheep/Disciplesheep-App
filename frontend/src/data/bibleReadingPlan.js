// Helper to get passage for any date
export const getBiblePassageForDate = (dateString) => {
  // First check if we have an explicit entry for this date
  const planEntry = bibleReadingPlan.find(entry => entry.date === dateString);
  
  if (planEntry) {
    return `${planEntry.book} ${planEntry.chapter}`;
  }
  
  // Calculate day offset from start date for fallback (cycling through plan)
  const startDate = new Date('2026-03-01');
  const currentDate = new Date(dateString);
  const daysDiff = Math.floor((currentDate - startDate) / (1000 * 60 * 60 * 24));
  
  // If date is before start, return the first entry
  if (daysDiff < 0) {
    return "Psalm 143";
  }
  
  // Cycle through our reading plan array
  const cycleIndex = daysDiff % bibleReadingPlan.length;
  const entry = bibleReadingPlan[cycleIndex];
  
  return `${entry.book} ${entry.chapter}`;
};
